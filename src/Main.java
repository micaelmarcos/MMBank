    private void btnCriaContaDialog2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriaContaDialog2ActionPerformed
        try {
            String nome = lblNomeCadastro.getText();
            String cpf = lblCPFCadastro.getText();
            double saldo = Integer.parseInt(lblDepositoInicial.getText());
            String senha = new String(lblSenhaCadastro.getPassword());
            numConta++;
            ContaBancaria c = new ContaBancaria(nome, senha, cpf, saldo, numConta);
            conta.add(c);
            salvarBackup();
            lblStatus.setText("Cadastro efetuado com sucesso!");
            lblNomeCadastro.setText("");
            lblCPFCadastro.setText("");
            lblDepositoInicial.setText("");
            lblSenhaCadastro.setText("");
            
            
        } catch(Exception e) {
            lblStatus.setText("Ocorreu um erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCriaContaDialog2ActionPerformed

    
