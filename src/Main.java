    public void salvarBackup() {
        String path = "C:\\Users\\MICAEL MARCOS\\Documents\\ADS 2° SEMESTRE\\PROGRAMAÇÃO ORIENTADA A OBJETOS\\UNIDADE 2\\MmBank_2\\src\\BackupContas.txt";
        
        try(BufferedWriter bw = new BufferedWriter(new FileWriter(path))) {
            for(ContaBancaria c : conta) {
                bw.write(c.getNome() + ","+c.getCPF()+"," + c.getSaldo()+"," + c.getSenha()+ ","+numConta);
                bw.newLine();
            }
        }
        catch(IOException e) {
            System.out.println(e);
        }
    
    }
    
    public void restauraBackup() {
        try(BufferedReader br = new BufferedReader(new FileReader("C:\\Users\\MICAEL MARCOS\\Documents\\ADS 2° SEMESTRE\\PROGRAMAÇÃO ORIENTADA A OBJETOS\\UNIDADE 2\\MmBank_2\\src\\BackupContas.txt"))){
            String line = br.readLine();
            conta.clear();

            while(line != null) {
                String[] fields = line.split(",");
                String nome = fields[0];
                String CPF = fields[1];
                double saldo = Double.parseDouble(fields[2]);
                String senha = fields[3];
                int numConta = Integer.parseInt(fields[4]);
                
                conta.add(new ContaBancaria(nome, senha, CPF, saldo, numConta));
                line = br.readLine();
            }
        }
        catch(IOException e) {
            System.out.println(e);
        }
    }
